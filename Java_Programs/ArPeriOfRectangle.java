class ArPeriOfRectangle 
{
	public static void main(String[] args) 
	{
		float width = 4.5f;
		float height = 7.9f;

		double ans = width*height;
		double perim=(width+height)*2;

		System.out.println("Area of rectangle is "+ ans);
		System.out.println("Area of perimeter is "+ perim);
	}
}
